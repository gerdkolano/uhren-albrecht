          <strong>Ticktack: Die Bahnhofsuhr geht jetzt richtig</strong>
          <br />
          
          Bad Nauheim (ihm).
          <br />
          Die historische Uhr am Bahnhof soll k&uuml;nftig korrekt laufen.  
          Zwei Ehrenamtliche aus Bad Nauheim wollen sich um die Wartung und 
          das Stellen k&uuml;mmern: Thomas Staller und Norbert Mischke.  
          Die Initiative ging von Innenarchitektin Beatrix van Ooyen aus.  
          Bei einem Besichtigungstermin machte sich 
          B&uuml;rgermeister Armin H&auml;user ein Bild.
          <br />
          
          In puncto Bahnhof gibt es sonst nur wenig Neues.  
          Das Geb&auml;ude geh&ouml;rt nach wie vor der Firma Portal Real.  
          Alleiniger Gesellschafter ist jetzt allerdings ein Rechtsanwalt aus Frankfurt.  
          Wie der Jurist auf Anfrage mitteilte, sucht er weiterhin nach P&auml;chtern.
          <br />

          Die Uhr ist ebenso alt wie der Bahnhof: etwa 100 Jahre.  
          Er wurde zur Zeit von Kaiser Wilhelm II.  gebaut.  
          Aufgrund ihres Alters geht die Uhr manchmal falsch.
          <br />
          Spezialist <span class="albrecht">Alexander Albrecht</span> hat sich vor drei Jahren intensiv 
          mit dem Problem befasst.
