<strong>Schmuckst&uuml;ck f&uuml;r die Kollwitz-Schule: 
        Historische Turmuhr restauriert</strong>
<br />
<br />

Zeilsheim. Wenn die Kinder dem
Unterricht in der K&auml;the-Kollwitz-Schule 
lauschen, wissen sie k&uuml;nftig
genau, welche Stunde ihnen geschlagen
hat. Zwar soll das Blinklicht
als Schulstunden-Signal nicht
abgeschafft werden, doch jetzt
werden die Sch&uuml;ler auch akustisch
auf jede volle Stunde aufmerksam
gemacht: Die Turmuhr wurde
aufwendig restauriert und zeigt
nicht nur die genaue Uhrzeit an,
sondern meldet sich auch
lautstark.
<br />

Vor einem Jahr wurde die Schule
samt ihrer Uhr 100 Jahre alt, Jetzt
hat sich endlich die Gelegenheit
ergeben, etwas Materielles zu
schenken&quot;, freute sich Bernd
Christ. Der Vorsitzende des Heimat-
und Geschichtsvereins hat in
Kooperation mit dem Vereinsring
ma&szlig;geblich zur neuen Uhr beigetragen.
&bdquo;Die Uhr hat mindestens
70 bis 80 Jahre geschwiegen&quot;, berichtet
Christ. Seit wann sie genau
au&szlig;er Betrieb gewesen sei, h&auml;tten
selbst die Heimatforscher nicht herausfinden
k&ouml;nnen.
<br />

Die Beteiligten sind auf jeden
Fall gl&uuml;cklich &uuml;ber das Schmuckst&uuml;ck.
Als &bdquo;kr&ouml;nenden Abschluss&quot;
der Umgestaltung der Schule bezeichnet
Vereinsring-Vorsitzender
Alfons Gerling die Uhr. &bdquo;Sie ist eine
Bereicherung f&uuml;r unser Schulgeb&auml;ude&quot;,
freut sich Schulleiter
Erich Sehr und f&uuml;gt schmunzelnd
an: &bdquo;Jetzt werden wir die Unterrichtszeit
auf die Uhr umstellen.&quot;
In Zukunft schl&auml;gt die computergesteuerte
Turmuhr von 8 bis 21 Uhr.
Bei den Anwohnern habe es fast
nur positive Resonanz gegeben,
berichtet Sehr. Bernd Christ
glaubt, &bdquo;dass der Klang nicht so gewaltig
ist, dass man in der Nachbarschaft
aus dem Bett f&auml;llt&quot;.
<br />

Die neue Uhr der Grund-, Hauptund
Realschule an der West-H&ouml;chster-Stra&szlig;e 
hat viele V&auml;ter:
Zun&auml;chst einmal war es Alexander
Schnabel, der das Ge b&auml;ude nach
&Uuml;bernahme des Hausmeister-Postens 
im Februar auf
&bdquo;Vordermann bringen wollte&quot;. Dabei
stie&szlig; er auch auf die heruntergekommene
Uhr im T&uuml;rmchen. Zudem
hat sie falsche Zeiten angezeigt.
Sie sei an den vier Seiten jeweils 
eine Viertelstunde zeitversetzt
gegangen, sagt Schnabel.
<br />

Im August machte der Hausmeister
den Heimat- und Geschichtsverein
auf das Problem
aufmerksam, Bernd Christ setzte
sich dann f&uuml;r die Restauration ein.
Gemeinsam mit dem Vereinsring
wurden Sponsoren gesucht und mit
Industriepark-Betreiber Infraserv,
der Volksbank H&ouml;chst sowie der
Frankfurter Sparkasse auch
gefunden. Insgesamt waren rund
1800 Euro f&uuml;r das neue Schlagwerk
der Uhr zu finanzieren. Die
restlichen 5500 Euro f&uuml;r den neuen
Zeigerantrieb und die Zifferbl&auml;tter
bezahlte die Stadt.
<br />

Die Frankfurter Firma von <span class="albrecht"> Alexander Albrecht </span>
wurde schlie&szlig;lich
im September mit der Instandsetzung
der neuen Uhr beauftragt.
Dabei stellte der Experte fest, dass
das Schlagwerk komplett fehlte.
<span class="albrecht"> Albrecht </span> montierte einen neuen
Hammer f&uuml;r die rund 160 Kilo
schwere Stahl-Glocke. Das Kreuzgetriebe
des Uhrwerks baute er
aus, reinigte es und versah es mit
neuen Schrauben. Die verblassten
Zifferbl&auml;tter komplett neu beschriftet.
Dabei entdeckte <span class="albrecht"> Albrecht </span>
auch mehrere Einschussl&ouml;cher, die
wohl aus Kriegszeiten stammen.
Au&szlig;erdem s&auml;uberte der Uhren-Restaurateur 
die enge Turmkammer
von einer breiten Schicht Taubendreck.
<br />

Doch vor der alten mechanischen
Uhr, die 1974 elektrifiziert worden
war, hat das moderne Technik-Zeitalter 
nicht Halt gemacht.
K&uuml;nftig werden sowohl das Schlag- als
auch das Uhrwerk per
Computer gesteuert. Das hat den
Vorteil, dass nicht beide Funktionen 
gleichzeitig ausfallen. Die Glocke 
k&ouml;nnte also noch weiterschlagen, 
wenn die Uhr nicht mehr l&auml;uft
- und umgekehrt. 
&bdquo;Die Uhr ist das I-T&uuml;pfelchen 
f&uuml;r dieses Geb&auml;ude&quot;,
freut sich <span class="albrecht"> Albrecht</span>. (wein)
<br />

Jetzt zeigt die Uhr wieder die
gleiche Zeit an vier Seiten.
