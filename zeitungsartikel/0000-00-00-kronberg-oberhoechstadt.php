von Katja Sperling 
<br />

Oberh&ouml;chstadt. Sie schl&auml;gt wieder, 
die Kirchturmuhr von St. Vitus. 
&bdquo;Manche Oberh&ouml;chst&auml;dter haben 
den Glockenschlag schon 
schmerzlich vermisst&quot;, hat Christa 
Lill, Mitarbeiterin im Pfarrb&uuml;ro, 
erfahren. &bdquo;Das Uhrwerk war nicht 
mehr funktionst&uuml;chtig. So konnten 
wir nur noch per Hand die Glocken 
zum Gottesdienst l&auml;uten.&quot; 
<br />

Zwecks Restaurierung und General&uuml;berholung 
waren das Ziffernblatt 
und das Uhrwerk f&uuml;r einige 
Wochen ausgebaut und bei der 
Frankfurter Firma Titelco general&uuml;berholt 
worden. &bdquo;Man konnte 
die goldenen r&ouml;mischen Zahlen mit 
blo&szlig;em Auge schon gar nicht mehr 
erkennen&quot;, so Uhrenrestaurator 
Uwe Kensa, der in seinem Berufsleben 
schon einiges erlebt und 
trotzdem immer mal wieder etwas 
zum Staunen hat - zum Beispiel 
die Einschussl&ouml;cher, die er auf dem 
schwarzen Ziffernblatt entdeckt 
hat. &bdquo;Sie stammen vermutlich von 
einem &uuml;berm&uuml;tigen Sch&uuml;tzen&quot;, 
meint er. 
<br />

Beauftragt wurde Kensa nicht 
von der Kirchengemeinde, sondern 
von der Stadt. &bdquo;Die Wartung der 
Uhr haben wir seit 1972, dem Jahr 
der Fusion, von der Gemeinde 
Oberh&ouml;chstadt &uuml;bernommen&quot;, 
erkl&auml;rte der Erste Stadtrat Wolf 
Dietrich Groote (CDU). &bdquo;Diese 
Kuriosit&auml;t hat ihren Ursprung im 19. 
Jahrhundert: Damals gab es eine 
Absprache mit der Gemeinde, dass 
diese den Pfarrer mit Brennholz 
versorgt. Dieses Deputat wurde 
in den 60er Jahren umgewandelt, 
da das Brennholz nicht mehr 
ben&ouml;tigt wurde&quot;, schilderte Groote. 
&bdquo;Die Gemeinde hat seitdem die 
Aufgabe, f&uuml;r die Wartung der 
Kirchturmuhr, die ein Herr Grosch 
der Kirchengemeinde 1967 gestiftet 
hatte, zu sorgen.&quot; Der Zahn der 
Zeit hat seitdem kr&auml;ftig an der 
Anlage genagt. 
<br />

&bdquo;Wir haben das Uhrwerk zwar 
ebenfalls von Staub und Dreck 
befreit, gleichzeitig aber eine 
Funkuhr installiert&quot;, so Kensa. 11000 
Mark hat die Stadt f&uuml;r die 
Sanierung der Kirchturmuhr 
investiert. Jetzt sind die
r&ouml;mischen Ziffern wieder erneuert
und frisch vergoldet. &bdquo;Die Uhrzeit
ist wieder klar und deutlich lesbar&quot;,
so Kensa. Um die beiden
Ziffernbl&auml;tter wieder an ihren 
angestammten Platz am Kirchturm 
zu installieren, war ein Spezial-
Hubwagen n&ouml;tig. &bdquo;Das war
Zentimeterarbeit&quot;, meinte Kensa.
<br />

