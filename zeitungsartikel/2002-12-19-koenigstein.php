Blick nach oben gen&uuml;gt
<br />

K&ouml;nigstein (ton) - Wissen Sie eigentlich,
was die Uhr geschlagen hat? Nein? Dann
werfen Sie einfach einen Blick auf den
gro&szlig;z&uuml;gig gestalteten und funkgesteuerten
Zeitmesser, der das ehemalige Rathaus in der
Hauptstra&szlig;e 15 ziert. Der private Investor
Eberhard Horn ist einer der ganz wenigen
Bauherren, die diesen Service f&uuml;r den B&uuml;rger -
von ihm privat finanziert - erm&ouml;glichen.
Und in K&ouml;nigstein ist es ohnehin schwer, einen
Blick auf eine &ouml;ffentliche Uhr zu werfen. Eine
solche befindet sich nur noch am Alten
Rathaus in der hinteren Hauptstra&szlig;e und an
der katholischen Kirche. Das ehemalige
Rathaus, das nach Abriss zu modernen
Gesch&auml;ftsr&auml;umen ausgebaut wird, ist ein
idealer Standort f&uuml;r eine &ouml;ffentliche Uhr, da
dieser Ort absolut zentral gelegen ist und fast
jeder K&ouml;nigsteiner, der im Ort unterwegs ist,
hier vorbei muss. Und im Hinblick auf das
kostbare Material aus Blattgold und auf den
Schriftzug &bdquo;Golden Years&quot;, der die Uhr
ziert, sagt Sponsor Eberhard Horn mit
philosophischem Hintergrund: &bdquo;In wirtschaftlich
schwierigen Zeiten wie den jetzigen,
kann man wohl kaum von goldenen Jahren
sprechen. Aber wie in Allem, beginnt auch
hier die erste Korrektur in eine bessere
Richtung, mit der Erwartung an k&uuml;nftig gl&auml;nzendere
Zeiten. Ungeachtet der wiederkehrenden
wirtschaftlichen Auf- und Abschw&uuml;nge
kann ohnehin jedes Jahr ein goldenes sein.
Oft vergehen goldene Jahre unbemerkt und
geben sich manchmal erst im R&uuml;ckblick auf
vergangene Zeiten zu erkennen. Ich hoffe,
dass diese Uhr &uuml;ber viele Jahre daran erinnert,
dass es goldene Jahre immer wieder geben
wird, oder dass das jeweilige sogar eines
ist&quot;.
<br />

Das wertvolle und kunstvoll gestaltete Unikat
wurde nach einem Entwurf von Eberhard
Horn von dem Frankfurter Uhrmachermeister
<span class="albrecht"> Alexander Albrecht</span>
fachm&auml;nnisch gefertigt.
&bdquo;Es gibt kaum noch private Bauherren, die
eine Uhr, und dann auch noch in dieser
Qualit&auml;t, der &Ouml;ffentlichkeit zur Verf&uuml;gung
stellen&quot;, 
wei&szlig; <span class="albrecht"> Albrecht</span> zu berichten, 
der unter anderem die Uhr am Frankfurter &bdquo;Theater
am Turm&quot; hergestellt hat. Zu welchem
Zeitpunkt die Uhr richtig funktioniert, liegt
nun freilich nicht mehr in des Uhrmachers
Hand. &bdquo;Das Geb&auml;ude ist ja noch eine
Baustelle und viele Gewerke m&uuml;ssen hier
noch arbeiten. So bald die Elektrik
funktioniert, geht auch diese wunderbare
Uhr auf die Sekunde genau&quot;. Wir freuen uns
darauf.
<br />

Der Bauherr des Geb&auml;udes in der Hauptstra&szlig;e 15, 
Eberhard Horn (links), 
entwarf eine wundersch&ouml;ne,
gro&szlig;e und &ouml;ffentliche Uhr, 
die jedem K&ouml;nigsteiner zeigt, was die Stunde geschlagen
hat. 
