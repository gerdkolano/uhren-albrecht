       <p style="font-family:Arial">
	    Auf die Sekunde genau l&auml;&szlig;t sich 
	    jetzt am Turm des ehemaligen Offenbacher 
	    Schlachthofs die Zeit ablesen: Die Bauherren 
	    des dortigen &bdquo;Wohnparks Buchh&uuml;gel&quot; haben 
	    dem denkmalgesch&uuml;tzten Geb&auml;ude einen 
	    neuen Chronometer spendiert. Die alte Mechanik 
	    wurde durch moderne Elektronik ersetzt, 
	    die Zeitsignale kommen per Funk, von 
	    Sommer-auf Winterzeit wird automatisch 
	    umgestellt. 
            <br />

	    Um die jeweils zehn Kilogramm 
	    schweren Zeiger der Uhr in 35 Metern H&ouml;he 
	    auszutauschen, mu&szlig;te extra ein alpiner Kletterspezialist 
	    verpflichtet werden. Der Mann habe seine 
	    Aufgabe bravour&ouml;s gel&ouml;st, so Heribert M&uuml;ller Salomon 
	    von LBS-Immobilien, die die 
	    Schlachthof-Wohnungen vermarkten. Allein 
	    der Minutenzeiger wiegt zehn Kilogramm. 
            <br />

	    Die Restauration und Modernisierung
	    des Uhrwerks wurde von der Firma 
            <span class="albrecht"> A. Albrecht </span> durchgef&uuml;hrt. 
