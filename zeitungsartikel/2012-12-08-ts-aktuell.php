          <strong>Im Ullsteinhaus tut sich was</strong>
       </p>
       <p style="font-family:Arial">
          Mit dem Ullsteinhaus geht
es voran: Nicht nur die
Uhren am Turm des Wahrzeichens
laufen wieder, auch die
Entwicklung des Deutschen
Pressemuseums steht nicht still.
Vor einem Jahr berichteten wir
&uuml;ber die Gr&uuml;ndung der privaten
Initiative zur Einrichtung eines
Deutschen Pressemuseums im
Ullsteinhaus. Nun gibt es weitere
handfeste Fortschritte.
Das Pressemuseum wird an
der Programmgestaltung des
Themenjahrs &raquo;Zerst&ouml;rte Vielfalt&laquo;
2013 teilnehmen und &ndash; 80 Jahre
nach der nationalsozialistischen
Machtergreifung &ndash; die Bedeutung
der Presse am Ende der
Weimarer Republik und zu Beginn
der NS-Diktatur w&uuml;rdigen.
Starke Kooperationspartner stehen
dabei bereit. Sowohl bei der
Stiftung Preu&szlig;ischer Kulturbesitz
als auch bei der Deutschen
Forschungsgemeinschaft st&ouml;&szlig;t
das Museumsprojekt auf gro&szlig;es
Interesse. In einer solchen
Kooperation k&ouml;nnen bedeutende
Sammlungen zur Pressegeschichte
zusammengetragen
und damit in Deutschland gehalten
werden. Die Entwicklung
der Zeitungslandschaft, des Verlagswesens
und der Pressefotografie
soll dabei nicht nur an
authentischem Ort der &Ouml;ffentlichkeit
gezeigt, sondern auch
weiter erforscht werden. F&uuml;r die
Einrichtung einer Forschungsstelle
im Rahmen des Pressemuseums
haben deshalb bereits
die HU und die FU ihre Mitarbeit
angeboten.
Auch das Berliner Abgeordnetenhaus
hat die Initiative erreicht.
Frank Zimmermann,
Abgeordneter im Wahlkreis
Tempelhof-S&uuml;d und Mariendorf-
Nord, fordert mit seinem
Antrag den Senat auf, die Zusammenarbeit
der verschiedenen
Partner aktiv zu unterst&uuml;tzen.
Bei diesem gemeinsamen
Engagement von privater
Initiative und Politik k&ouml;nnte das
Deutsche Pressemuseum im
Ullsteinhaus schon bald Realit&auml;t
sein. F&uuml;r unseren Bezirk
w&auml;re es ein kulturelles Highlight,
das die Tradition der Zeitungsstadt
Berlin und die
St&auml;rkung des Verlagsstandortes
Berlin auf einzigartige Weise
verbindet.
Lars Rauchfuss

