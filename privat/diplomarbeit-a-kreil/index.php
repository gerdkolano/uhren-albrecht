<div>&Uuml;berlegungen zur Erhaltung von Funktionalit&auml;t und historischer Substanz</div> 

<div>1. Gutachter: Prof. Dr. Christian Stadelmann<br /> 
2. Gutachter: Diplom-Restaurator (FH) Dietmar Linke<br /> 
<br /> 
Die vorliegende Diplomarbeit befasst sich mit der 
Konservierung und Restaurierung einer Turmuhr aus 
der kleinen Kirchengemeinde Uetz n&ouml;rdlich von Potsdam. 
Die Uhr stammt aus der bedeutenden Turmuhrfabrik C. F. Rochlitz in Berlin. 
Zeitlich kann die Uhr zwischen 1897 und 1906 eingeordnet werden. 
Sie ist ein Beispiel f&uuml;r die industrielle Fertigung in Kleinserien mit 
Dreh- und Fr&auml;smaschinen. 
Nach ausf&uuml;hrlicher Dokumentation der Bauteile und der 
Funktionsweise anhand von Zeichnungen wird diskutiert, 
inwieweit die Erhaltung der Funktion, 
ohne zu gro&szlig;e Eingriffe auf die Originalsubstanz m&ouml;glich ist. 
Es wird eine eingeschr&auml;nkte Erhaltung der Funktionalit&auml;t nur f&uuml;r den 
kurzzeitigen Vorf&uuml;hrbetrieb angestrebt. 
Abschlie&szlig;end werden die Restaurierungsma&szlig;nahmen theoretisch er&ouml;rtert und 
die praktische Durchf&uuml;hrung beschrieben, 
die zum gr&ouml;&szlig;ten Teil eine partielle Entfernung der Korrosionsprodukte 
mit einem Niederdruckstrahlger&auml;t vorsieht.
</div>

<p>
Um das Archivgut zu schützen und zu pflegen,
unterhält das Filmmuseum Potsdam eine
eigene Restaurierungswerkstatt. Dies hat
gegenüber anderen Museen, die mehr und
mehr Fremdfirmen in Anspruch nehmen, den
Vorzug, daß der verantwortliche Restaurator
Dietmar Linke seinen Bereich genau kennt
und meistens vor Ort
ist. Er kann zum einen
langfristige Planungen zur Bestandserhaltung verfolgen und andererseits dringliche
Sicherungsmaßnahmen in kürzester Zeit vornehmen.
</p>
