<h3>
Im Naunhofer 
Turmuhrenmuseum ist man 
auf der H&ouml;he der Zeit. 
</h3>

VON AXEL SCHEIBE 
<br />

Da soll es doch wirklich Leute geben, 
die st&ouml;rt schon das Ticken des 
Weckers. Die sind nat&uuml;rlich gl&uuml;cklich 
&uuml;ber moderne Uhren, die mit ihrer 
Elektronik auf jegliche Feinmechanik 
verzichten, sprich v&ouml;llig ger&auml;uschlos 
ihren Dienst versehen. Andere 
hingegen sind fasziniert von Uhren des 
alten Stils. Da kann man die 
handwerkliche Meisterschaft der 
Uhrmacher 
noch so richtig mit H&auml;nden greifen. F&uuml;r 
all jene ist das Turmuhrenmuseum in der 
Gemeinde Naunhof, gar nicht weit von 
Leipzig entfernt, genau die richtige 
Adresse. Hier tickt es kr&auml;ftig. Historische 
Turmuhren sind mit ihrem 
Ger&auml;uschpegel nicht so sch&uuml;chtern wie 
der Wecker auf dem Nachttisch. 
Immerhin m&uuml;ssen sie eine ganze Menge 
Stahl bzw. Gusseisen in Bewegung 
halten. Das Museum residiert in einem 
150-j&auml;hrigen H&auml;uschen, der ehemaligen 
M&auml;dchenschule, direkt an der Kirche des 
Ortes. Man findet es leicht. Im 
Gegensatz zu der Mehrzahl seiner 
Exponate ist es selbst noch recht jung. 
Vor reichlich f&uuml;nf Jahren wurde es von 
Sachsens Ministerpr&auml;sident Kurt 
Biedenkopf, der sich bei dieser 
Gelegenheit selbst als Turmuhrenfan 
outete, feierlich er&ouml;ffnet. 
Vorausgegangen war die jahrelange 
Sammlerleidenschaft von Helga und 
Peter Schnabel. Peter Schnabel, seines 
Zeichens (Turm-)uhr-macher, hatte das 
Sammelfieber schon vor Jahrzehnten erfasst. 
Immer wenn es hie&szlig;, ein altes 
Turmuhrenwerk durch ein neues, meist 
auch servicefreundlicheres zu ersetzen, 
nahm er das alte, ausgetauschte St&uuml;ck 
unter seine Fittiche. Gemeinsam mit 
Frau Helga brachte er es wieder in 
Schwung. Doch mit der Zeit wurde 
dieses Hobby f&uuml;r h&auml;usliche Verh&auml;ltnisse 
ganz einfach zu umfangreich. Immerhin 
sind die alten Meisterwerke der 
Mechanik nicht gerade leicht und auch 
nicht klein. Also gr&uuml;ndete man einen 
Uhrenmuseum-Verein, und nach Jahren 
der Vorbereitung und der Suche 
geeigneter R&auml;umlichkeiten war es 1995 
soweit, die Exposition konnte ihre 
Pforten &ouml;ffnen. 
<br />
<br />

Wer nach Naunhof kommt, findet 
ein modernes Museum mit einer 
Vielzahl an Exponaten aus der
Vergangenheit. &Auml;ltestes St&uuml;ck ist 
eine Leihgabe des Dresdner 
Mathematisch-Physikalischen 
Salons. Ein gusseisernes Lauf-
und Schlagwerk aus dem Jahre 
1606, das vormals in einem 
Schlossturm f&uuml;r die richtige Zeit 
sorgte. 
<br />
<br />

Auch aus dem 18. und 19. 
Jahrhundert k&ouml;nnen Turmuhren 
pr&auml;sentiert werden. So das 
Uhrwerk der Turmuhr aus dem 
Rittergut in Schletta bei Mei&szlig;en, 
das dort seit 1850 in Betrieb war. 
Die Mehrzahl der Ausstellungsst&uuml;cke 
stammt jedoch aus den 
Jahren um 1900. Da konnten 
Schnabels zahlreiche interessante 
Werke retten und wieder in alte 
Sch&ouml;nheit zur&uuml;ckversetzen. 
Faszinierend die feinmechanische 
Pr&auml;zision, die in den gro&szlig;en 
Uhrwerken versteckt ist. 
<br />
<br />

Immerhin sind diese Kolosse 
&auml;hnlich genau gegangen wie 
unsere modernen Taschenuhren. 
Da stimmt jedes Zahnrad, jede 
Welle und jede Feder. Fragt man 
Manuela Langer, die Museumsf&uuml;hrerin, 
nach ihren Lieblingsst&uuml;cken, 
so wird sie gleich im 
Erdgeschoss f&uuml;ndig.
